  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  /* USER CODE BEGIN 2 */

  uint32_t ud;

  // get existing user data bytes - note, it is actually a single byte each time but the API returns a uint32_t
  ud = HAL_FLASHEx_OBGetUserData(OB_DATA_ADDRESS_DATA0);
  ud = HAL_FLASHEx_OBGetUserData(OB_DATA_ADDRESS_DATA1);

  // create and fill in a representation of the non-user data parts of the option bytes
  FLASH_OBProgramInitTypeDef option_bytes;
  HAL_FLASHEx_OBGetConfig(&option_bytes);

  // set the bytes we want to write back, this must include the non-user data parts of the option bytes as they will be erased
  option_bytes.OptionType = OPTIONBYTE_WRP | OPTIONBYTE_RDP | OPTIONBYTE_USER | OPTIONBYTE_DATA;

  // set which user data byte to write and its value for the first user data byte
  option_bytes.DATAAddress = OB_DATA_ADDRESS_DATA0;
  option_bytes.DATAData = 0x55;

  // unlock flash
  HAL_FLASH_Unlock();
  HAL_FLASH_OB_Unlock();

  // write the option bytes (non-user data parts and 1 user data byte), this does an erase first internally when
  // writing the non-user data option bytes
  HAL_FLASHEx_OBProgram(&option_bytes);

  // set which user data byte to write and its value for the second user data byte
  option_bytes.DATAAddress = OB_DATA_ADDRESS_DATA1;
  option_bytes.DATAData = 0xcc;

  // no need to do another erase so no need to write all the non-user data option bytes and as
  // only writing a user data byte this time choose just that and then write it
  option_bytes.OptionType = OPTIONBYTE_DATA;
  HAL_FLASHEx_OBProgram(&option_bytes);

  // lock the flash again
  HAL_FLASH_OB_Lock();
  HAL_FLASH_Lock();

  // re-read the user data 2 bytes again to check
  ud = HAL_FLASHEx_OBGetUserData(OB_DATA_ADDRESS_DATA0);
  ud = HAL_FLASHEx_OBGetUserData(OB_DATA_ADDRESS_DATA1);
  (void)ud;

